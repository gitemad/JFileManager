package Model;

public class NavigateButtonModel {
	
	private boolean enable;
	
	public NavigateButtonModel(boolean enable) {
		this.enable = enable;
	}
	

	/**
	 * @return the enable
	 */
	public boolean isEnable() {
		return enable;
	}

	/**
	 * @param enable the enable to set
	 */
	public void setEnable(boolean enable) {
		this.enable = enable;
	}
	
	
}
